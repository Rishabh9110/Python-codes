             #methods in list
#method-1(sort-use for sorting the given list)
friends=[10,12,5,345.06,95,203,1]
friends.sort()#this will sort the list 
print(friends)
#method-2(reverse-sort the list in decending order)
friends.reverse()
print(friends)
#method-3(insert-this will insert a element in the list at the given index)
friends.insert(3,1000)
print(friends)
#method-4(pop-remove the element from the given index)
friends.pop(3)
print(friends)
#method-5(remove-remove the element you give)
friends.remove(95)
print(friends)